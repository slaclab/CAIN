C**************************** DERFC *********************************
      FUNCTION DERFC(X)
C  Erfc(x)= [2/sqrt(pi)] * [integral exp(-t**2) dt  from t=x to infty]
C   Erfc(infty)=0, Erfc(-infty)=2.
C   Efrc(x)=1-Erf(x)
      PARAMETER (MA=11,MBN=8,MBD=9)
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 A(0:MA),BN(0:MBN),BD(MBD)
      DATA (A(I),I=0,MA)/
     A   1.128379167095512574D00,   -3.761263890318359638D-1,
     B   1.128379167094621337D-1,   -2.686617064335569048D-2,
     C   5.223977607744557810D-3,   -8.548325995334072919D-4,
     D   1.205529529690233942D-4,   -1.492474166693243225D-5,
     E   1.644745842451230273D-6,   -1.620859836516383222D-7,
     F   1.372008421427857357D-8,   -7.795244458089992761D-10/
      DATA (BN(I),I=0,MBN)/
     A   5.641895835477562869D-1,    7.062273683751204381D00,
     B   4.662671269808312448D+1,    1.990808818123393773D+2,
     C   5.899839038827645351D+2,    1.230520184269723550D+3,
     D   1.760028963041555533D+3,    1.585520458551960309D+3,
     E   6.986974844099113885D+2/
      DATA (BD(I),I=1,MBD)/
     A   1.251755418691370499D+1,    8.314369647671238083D+1,
     B   3.591204527119782086D+2,    1.086541090120516631D+3,
     C   2.351212319259538659D+3,    3.602357420397171251D+3,
     D   3.740005936371413548D+3,    2.373916881911665387D+3,
     E   6.986974004764069309D+2/
      DATA X0/1.0D0/
C
      XA=ABS(X)
      IF(XA.GE.X0) GOTO 300
      XX=X**2
      F=A(MA)
      DO 220 I=MA-1,0,-1
  220 F=A(I)+XX*F
      DERFC=1D0-X*F
      RETURN
  300 IF(X.LE.-6.1D0) THEN
        DERFC=2D0
        RETURN
      ELSEIF(X.GE.26.5D0) THEN
        DERFC=0D0
        RETURN
      ENDIF
      Z=1/XA
      FN=BN(MBN)
      FD=BD(MBD)
      DO 320 I=MBN-1,0,-1
  320 FN=BN(I)+Z*FN
      DO 340 I=MBD-1,1,-1
  340 FD=BD(I)+Z*FD
      DERFC=EXP(-XA**2)*FN/(XA+FD)
      IF(X.LT.0) DERFC=2-DERFC
      RETURN
      END
